        <h3>PORK CHOPS WITH COFFEE DRY RUB PRINT</h3> 
        <p>8 servings</p>
        <p>Cook time: 15 minutes Prep time: 5 minutes</p>
        
        <p>Ingredients</p>
        <uL>
            <li>4 teaspoons chili powder</li>
            <li>4 teaspoons ground coffee</li>
            <li>2 tablespoons dark brown sugar</li>
            <li>4 teaspoons salt</li>
            <li>6 medium, bone-in pork chops about 1-inch thick</li>
        <li>¼ teaspoon turmeric</li>
        <li>¼ teaspoon ground coriander</li>
        <li>¼–½ teaspoon cayenne pepper</li>
        <li>½ teaspoon freshly ground pepper</li>
        <li>½ teaspoon garlic powder</li>
        <li>½ teaspoon onion powder</li>
    </uL>

        <p>Instructions</p>

        <p>In a small bowl, combine all the ingredients and stir until well blended. Preheat the grill.</p>

        <p>Generously rub both sides of the pork chops with the coffee rub. Grill the pork chops for about 5 to 7 minutes on each side over a medium heat.</p>

        <p>Let the chops rest covered with aluminum foil before serving. Discard any unused rub.</p>
    
