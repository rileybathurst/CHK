        <h3>Grilled Venison Loin (Backstrap) With Horseradish Cream</h3> 
        <p><em>“He that strikes the venison first shall be the lord o’ the feast.” — Shakespeare, King Lear</em></p>
        
        <p>Ingredients</p>
        <uL>
            <li>1.5 to 2 pounds venison loin, trimmed of silver skin</li>
            <li>kosher salt and freshly ground black pepper</li>
            <li>2 tablespoons chopped fresh herbs, like rosemary, thyme, and oregano</li>
            <li>3 tablespoons olive oil</li>
            <li>1 cup crème fraîche</li>
            <li>2 tablespoons freshly grated or prepared horseradish</li>
            <li>2 teaspoons chopped fresh</li>
            <li>chives or parsley</li>
            <li>juice and zest of 1 lemon</li>
    </uL>

    <p>Instructions</p>

    <p>Season the meat with salt and pepper. In a small bowl, mix herbs with the olive oil, then spread all over the meat. Refrigerate for at least 2 hours.</p>

    <p>Make a hot fire in a charcoal grill or set a gas grill on high. Grill the meat on one side, without moving it, 4 to 5 minutes, then rotate 90 degrees to make grill marks and char more surface area. Flip the loin and cook 4 to 5 minutes more. It should have a deep-brown char on the outside but still be rare on the inside. Transfer to a warm plate and allow to rest for 10 minutes.</p>

    <p>Mix the crème fraîche, horseradish, chives, lemon juice, and zest in a small bowl and season with salt. Serve the loin, thinly sliced against the grain, with the sauce.</p>
    
